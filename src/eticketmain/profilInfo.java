    void bookingInfo() {
        String query = "select * from USERLIST where username='" + userName + "'";
        String tId = "";
        try {
            Connect con1 = new Connect();
            Statement stat = con1.st;
            ResultSet res = stat.executeQuery(query);
            if (res.next()) {
                String transId = String.valueOf(res.getString("TransportId"));
                String tBooked = String.valueOf(res.getString("Ticket_Booked"));
                jLabel15.setText(transId);
                jLabel20.setText(tBooked);
                tId = transId;
            } else {
                jLabel15.setText("");
                jLabel20.setText("");
            }
        } catch(Exception e) {
            System.out.println(e);
        }
        String query1="select * from Transport where trans_id='"+ tId +"'";
        try {
            Connect cons = new Connect();
            Connection cont = cons.con;
            Statement statement = cont.createStatement();
            ResultSet result = statement.executeQuery(query1);
            if (result.next()) {
                String from = String.valueOf(result.getString("source"));
                String to  = String.valueOf(result.getString("destination"));
                String currT = String.valueOf(result.getString("time"));
                String currDate = String.valueOf(result.getString("date"));
                jLabel16.setText(from);
                jLabel17.setText(to);
                jLabel18.setText(currDate);
                jLabel19.setText(currT);
            } else {
                jLabel16.setText("");
                jLabel17.setText("");
                jLabel18.setText("");
                jLabel19.setText("");
            }
        } catch(Exception e) {
            System.out.println(e);
        }
    }
