        // TODO add your handling code here:
        char[] oldp = jPasswordField1.getPassword();
        char[] newp = jPasswordField2.getPassword();
        char[] confp = jPasswordField3.getPassword();
        String oldPassword = new String(oldp);
        String newPassword = new String(newp);
        String confPassword = new String(confp);
        String query = "select * from DATA where username='" + userName + "'";
        try {
            Connect connection = new Connect();
            Statement statement = connection.st;
            ResultSet result = statement.executeQuery(query);
            String currpass = "";
            if (result.next()) {
                currpass = String.valueOf(result.getString("password"));
            }
//            JOptionPane.showMessageDialog(null, currpass + " " + oldPassword);
            if (currpass.equals(oldPassword) == false) {
                JOptionPane.showMessageDialog(null, "Password incorrct!");
            } else if (newPassword.equals(confPassword) == false) {
                JOptionPane.showMessageDialog(null, "Password unmatched");
            } else {
                String chngQuery = "update DATA set password='" + newPassword + "' where username = '" + userName + "'";
                try {
                    Connect conc = new Connect();
                    Connection currcon = conc.con;
                    Statement newstat = currcon.createStatement();
                    newstat.executeUpdate(chngQuery);
                    JOptionPane.showMessageDialog(null, "Passsword changed!");
                } catch(Exception e) {
                    System.out.println(e);
                }
            }
        } catch(Exception e) {
            System.out.println(e);
        }
